package day09_scannaer_practice;

public class RemainderOperator {
    public static void main(String[] args) {
        System.out.println(10 / 3);  //3
        System.out.println(10 % 3);  //1 3+3+3 so it have 1 left
        System.out.println(10 / 5);  //2
        System.out.println(10 % 5);  //0

        System.out.println(20 / 7);  //2
        System.out.println(10.0 / 3);  //3.33 if you want exactly number.
        System.out.println(20 % 7);  //6
    }
}
