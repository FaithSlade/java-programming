package day05_primitives_concatanation;

public class PrimitivesExample {
    public static void main(String[] args){

//        ***** WHOLE NUMBER/INTEGER TYPES *****
        byte byteValue = 127;
        short shortValue = 32767;
        int intValue = 2147483647;
        long longValue = 922337203876555699l;

//        ***** DECIMAL NUMBERS/FLOATING POINT TYPE *****
        float floatValue = 2345.55F;
        double doubleValue = 27.3644728655343256376843784678798769;
        // 235467.3644728D also valid

//        ***** CHARACTER TYPE *****
        char charValue = 'a';
        //it can be A|a or number and it need to be one character only

//       ***** BOOLEAN TYPE ***** - true|false
        boolean booleanValue = true; // true|false in lower case

//        PRINT VARIABLE VALUES
        System.out.println(byteValue);
        System.out.println(shortValue);
        System.out.println(intValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(charValue);
        System.out.println(booleanValue);




    }
}
